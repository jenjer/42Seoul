int	ft_sqrt(int n);

int	ft_sqrt(int n)
{
	int	i;

	if (n <= 0)
		return (0);
	i = 1;
	while (i <= n / i)
	{
		if (i * i == n)
			return (i);
		i++;
	}
	return (0);
}
